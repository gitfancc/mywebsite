package com.appscomm.sport.vo;

import java.io.Serializable;


public class SleepRecordStatistic implements Serializable{
	private static final long serialVersionUID = 3232131254047564506L;
	private long personId;
	private String deviceType;//设备类型
	private int quality;//睡眠质量 0-100
	private float sleepDuration;//睡眠持续时间  单位：小时
	private float awakeDuration;//清醒时长 单位：小时
	private float lightDuration;//浅睡时长 单位：小时
	private float deepDuration;//深睡时长 单位：小时
	private float totalDuration;//床上总时长 单位：小时
	private int awakeCount;//唤醒次数 单位：次
	private String startTime;
	private String endTime;
	private String days;
	private String weeks;
	private String months;
	public long getPersonId() {
		return personId;
	}
	public void setPersonId(long personId) {
		this.personId = personId;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public float getSleepDuration() {
		return sleepDuration;
	}
	public void setSleepDuration(float sleepDuration) {
		this.sleepDuration = sleepDuration;
	}
	public float getAwakeDuration() {
		return awakeDuration;
	}
	public void setAwakeDuration(float awakeDuration) {
		this.awakeDuration = awakeDuration;
	}
	public float getLightDuration() {
		return lightDuration;
	}
	public void setLightDuration(float lightDuration) {
		this.lightDuration = lightDuration;
	}
	public float getDeepDuration() {
		return deepDuration;
	}
	public void setDeepDuration(float deepDuration) {
		this.deepDuration = deepDuration;
	}
	public float getTotalDuration() {
		return totalDuration;
	}
	public void setTotalDuration(float totalDuration) {
		this.totalDuration = totalDuration;
	}
	public int getAwakeCount() {
		return awakeCount;
	}
	public void setAwakeCount(int awakeCount) {
		this.awakeCount = awakeCount;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getWeeks() {
		return weeks;
	}
	public void setWeeks(String weeks) {
		this.weeks = weeks;
	}
	public String getMonths() {
		return months;
	}
	public void setMonths(String months) {
		this.months = months;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	
}
