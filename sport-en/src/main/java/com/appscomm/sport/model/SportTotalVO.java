package com.appscomm.sport.model;

import java.io.Serializable;

public class SportTotalVO implements Serializable {
	private static final long serialVersionUID = 61385192112755292L;
	private int steps;
	private double cal;
	private double dist;
	private int totaltime;
	private String days;
	private String hours;
	private String weeks;
	private String months;
	public int getSteps() {
		return steps;
	}
	public void setSteps(int steps) {
		this.steps = steps;
	}
	public double getCal() {
		return cal;
	}
	public void setCal(double cal) {
		this.cal = cal;
	}
	public double getDist() {
		return dist;
	}
	public void setDist(double dist) {
		this.dist = dist;
	}
	public int getTotaltime() {
		return totaltime;
	}
	public void setTotaltime(int totaltime) {
		this.totaltime = totaltime;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getWeeks() {
		return weeks;
	}
	public void setWeeks(String weeks) {
		this.weeks = weeks;
	}
	public String getMonths() {
		return months;
	}
	public void setMonths(String months) {
		this.months = months;
	}
	@Override
	public String toString() {
		return "SportTotalVO [steps=" + steps + ", cal=" + cal + ", dist=" + dist + ", totaltime=" + totaltime + ", days=" + days + ", hours=" + hours + ", weeks=" + weeks + ", months=" + months + "]";
	}
	
}
