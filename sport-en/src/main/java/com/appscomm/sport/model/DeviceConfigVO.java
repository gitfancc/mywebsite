package com.appscomm.sport.model;

import java.io.Serializable;

public class DeviceConfigVO implements Serializable{
	private static final long serialVersionUID = -1525353209942794642L;
	private String watchId;
	private String brightness;
	private Integer ringMode;
	private Integer volume;
	public String getWatchId() {
		return watchId;
	}
	public void setWatchId(String watchId) {
		this.watchId = watchId;
	}
	public String getBrightness() {
		return brightness;
	}
	public void setBrightness(String brightness) {
		this.brightness = brightness;
	}
	public Integer getRingMode() {
		return ringMode;
	}
	public void setRingMode(Integer ringMode) {
		this.ringMode = ringMode;
	}
	public Integer getVolume() {
		return volume;
	}
	public void setVolume(Integer volume) {
		this.volume = volume;
	}
		
}
