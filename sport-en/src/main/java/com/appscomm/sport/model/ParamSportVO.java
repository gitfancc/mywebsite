package  com.appscomm.sport.model;
import java.io.Serializable;
/**
 *  运动数据(开始时间，结束时间，步数，距离，速度，卡路里，平均心率)
 *  用于搜索运动记录，web显示Highcharts图表
 *  创建日期：2013-09-18  15：20
 * @author kuangzhenming
 *
 */
 public  class ParamSportVO implements Serializable{

      private static final long serialVersionUID = -201309181506004140L;
      private String watchId;
      private String startTime;
      private String endTime;
      private int steps;
      private float dist;
      private float speed;
      private float cal;
      private int deviceId;
      private int avgRate;
      private int minRate;
      private int maxRate;
      
      /**
       * 获取表ID
       * @return
       */
      public String getWatchId(){
         return this.watchId;
      }
      /**
       * 设置表ID
       * @param WatchId
       */
      public void setWatchId(String WatchId){
         this.watchId=WatchId;
      }
      /**
       * 获取开始时间
       * @return
       */
      public String getStartTime(){
         return this.startTime;
      }
      /**
       * 设置开始时间
       * @param Starttime
       */
      public void setStartTime(String Starttime){
         this.startTime=Starttime;
      }
      /**
       * 获取结束时间
       * @return
       */
      
      public String getEndTime(){
         return this.endTime;
      }
      /**
       * 设置结束时间
       * @param Endtime
       */
      public void setEndTime(String Endtime){
         this.endTime=Endtime;
      }
      /**
       * 获取步数
       * @return
       */
      public int getSteps(){
         return this.steps;
      }
      /**
       * 设置步数
       * @param Steps
       */
      public void setSteps(int Steps){
         this.steps=Steps;
      }
      /**
       * 获取距离
       * @return
       */
      public float getDist(){
         return this.dist;
      }
      /**
       * 设置距离
       * @param Dist
       */
      public void setDist(float Dist){
         this.dist=Dist;
      }
      /**
       * 获取速度
       * @return
       */
      public float getSpeed(){
         return this.speed;
      }
      /**
       * 设置速度
       * @param Speed
       */
      public void setSpeed(float Speed){
         this.speed=Speed;
      }
      /**
       * 获取卡路里
       * @return
       */
      public float getCal(){
         return this.cal;
      }
      /**
       * 设置卡路里
       * @param Cal
       */
      public void setCal(float Cal){
         this.cal=Cal;
      }
      /**
       * 获取设备型号ID，和watchId多对一关系
       * @return
       */
      public int getDeviceId(){
         return this.deviceId;
      }
      /**
       * 设置设备型号ID，和watchId多对一关系
       * @param Deviceid
       */
      public void setDeviceId(int DeviceId){
         this.deviceId=DeviceId;
      }
      /**
       * 获取平均心率
       * @return
       */
      public int getAvgRate(){
         return this.avgRate;
      }
      /**
       * 设置平均心率
       * @param Avgrate
       */
      public void setAvgRate(int AvgRate){
         this.avgRate=AvgRate;
      }
      /**
       * 获取最小心率
       * @return
       */
      public int getMinRate(){
         return this.minRate;
      }
      /**
       * 设置最小心率
       * @param Minrate
       */
      public void setMinRate(int MinRate){
         this.minRate=MinRate;
      }
      /**
       * 获取最大心率
       * @return
       */
      public int getMaxRate(){
         return this.maxRate;
      }
      /**
       * 设置最大心率
       * @param Maxrate
       */
      public void setMaxRate(int MaxRate){
         this.maxRate=MaxRate;
      }
   }


